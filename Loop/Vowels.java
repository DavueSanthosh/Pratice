public class Vowels {

	public static void main(String[] args) {
        char name='S';
        
        switch(name) {
        
        case'A':
        case'E':
        case'I':
        case'O':
        case'U':
        	
        	System.out.println("the S is Vowels");
        break;	
        
        default:
        	System.out.println("the S not vowels");
        	
        }
	}

}
