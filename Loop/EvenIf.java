package Loop;

public class EvenIf {

	public static void main(String[] args) {
		
      for (int index = 1; index <=200; index++) {
    	  
    	  if(index % 2==0) {
    		  
		System.out.println("the number is even:"+index);
	}
    	  else {
    		  System.out.println("the number is odd:"+index);
    	  }
	}

}
}